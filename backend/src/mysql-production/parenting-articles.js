function buildArticleContent(categoryConfig, topic, ageVariant) {
  const steps = [
    `1. 在${topic.scene}，先把场景和目标说清楚，帮助孩子知道这一次准备练什么。`,
    `2. 用一句短句聚焦目标“${topic.goal}”，例如“${topic.example}”。`,
    `3. 结合${ageVariant.label}阶段特点，${ageVariant.actionHint}`,
    '4. 结束后用两到三句话复盘这次哪里有效、下次准备继续怎么做。'
  ].join('\n');

  const observe = topic.observe.map((item) => `- ${item}`).join('\n');
  const pitfalls = topic.pitfalls.map((item) => `- ${item}`).join('\n');
  const authorities = [
    `- 本文建议综合参考${categoryConfig.authorities.join('、')}关于${topic.evidenceFocus}的指南、共识和家庭养育建议。`,
    '- 实操上优先使用稳定作息、低刺激环境、共情回应、分步骤练习和一致边界五类循证策略。',
    '- 若出现持续功能受损、明显发育倒退、伤人伤己或睡眠进食显著恶化，应及时面诊儿科、儿童保健科或儿童心理专科。'
  ].join('\n');

  return [
    '## 核心结论',
    `${topic.summary}${ageVariant.summarySuffix}`,
    '## 这个年龄段为什么要这样做',
    `${ageVariant.development}${topic.why}`,
    '## 家庭操作步骤',
    steps,
    '## 家长观察重点',
    observe,
    '## 常见误区',
    pitfalls,
    `【家长提示】${topic.tip}`,
    `【转介边界】${topic.referral}`,
    '## 证据依据',
    authorities
  ].join('\n\n');
}

function buildArticle(categoryConfig, topic, ageVariant, index) {
  return {
    title: `${ageVariant.label}孩子${topic.shortTitle}：${topic.titleSuffix}`,
    summary: `${topic.summary}结合${ageVariant.label}阶段特点给出家庭可执行步骤。`,
    category: categoryConfig.category,
    sub_category: topic.sub_category,
    age_group: ageVariant.label,
    tags: [categoryConfig.category, topic.sub_category, ageVariant.label].concat(topic.tags || []).join(','),
    author: EDITOR,
    evidence_level: 'A',
    content: buildArticleContent(categoryConfig, topic, ageVariant),
    sort_order: index + 1
  };
}

const PARENTING_ARTICLES = CATEGORY_LIBRARY.flatMap((categoryConfig) => {
  return AGE_VARIANTS.flatMap((ageVariant) => {
    return categoryConfig.topics.map((topic, index) => buildArticle(categoryConfig, topic, ageVariant, index));
  });
});

