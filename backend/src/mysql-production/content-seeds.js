function buildTaskSteps(subjectCode, objective, customSteps) {
  if (customSteps) {
    return customSteps;
  }
  if (subjectCode === 'reading_comprehension') {
    return ['先快速浏览材料', '找出和任务目标最相关的线索', '回答家长提问', '最后用一句话复述重点'].join('\n');
  }
  if (subjectCode === 'logical_thinking') {
    return ['先观察题目中的信息', '说出你发现的规律、分类或判断标准', '完成比较或排序', '解释你为什么这样想'].join('\n');
  }
  if (subjectCode === 'expression_communication') {
    return ['先想一想最想说的重点', '开口说出核心内容', '补充一个理由、顺序或例子', '再完整说一遍让别人听懂'].join('\n');
  }
  if (subjectCode === 'learning_metacognition') {
    return ['开始前先看清任务', '说出准备怎么做', '完成后检查关键点', '复盘这次哪一步最有效'].join('\n');
  }
  return ['先观察当前情境', '提出一个猜想、问题或办法', '说出你的理由', '再想一种不同的可能或改进方案'].join('\n');
}

function joinTaskContentSections(sections) {
  return sections.filter(Boolean).join('\n');
}

