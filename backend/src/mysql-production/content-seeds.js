function buildReadingTasks() {
  const tasks = [];
  const subjectOrder = ['reading_comprehension', 'logical_thinking', 'expression_communication', 'learning_metacognition', 'inquiry_creativity'];
  for (const ageProfile of AGE_PROFILES) {
    for (const subjectCode of subjectOrder) {
      const items = SUBJECT_TASK_LIBRARY[subjectCode] || [];
      for (const item of items) {
        const baseDifficulty = subjectCode === 'reading_comprehension' || subjectCode === 'logical_thinking' ? 1 : 2;
        tasks.push({
          task_code: `${SUBJECT_PREFIX[subjectCode]}_${ageProfile.code}_${item[0]}`,
          title: item[1],
          subject_code: subjectCode,
          age_range: ageProfile.age_range,
          difficulty: Math.min(4, baseDifficulty + ageProfile.difficulty_offset),
          duration: ageProfile.duration,
          material: item[2],
          objective: item[3],
          steps: buildTaskSteps(subjectCode, item[3], item[7]),
          parent_prompt: item[4],
          content: buildTaskContent(subjectCode, ageProfile, item[0], item[1], item[3], item[4], item[5]),
          tips: `${item[6]} ${SUBJECT_TIPS[subjectCode]}`,
          example_answer: item[5]
        });
      }
      const extraItems = SUBJECT_TASK_EXPANSIONS[subjectCode] || [];
      for (const item of extraItems) {
        const baseDifficulty = subjectCode === 'reading_comprehension' || subjectCode === 'logical_thinking' ? 1 : 2;
        tasks.push({
          task_code: `${SUBJECT_PREFIX[subjectCode]}_${ageProfile.code}_${item[0]}`,
          title: item[1],
          subject_code: subjectCode,
          age_range: ageProfile.age_range,
          difficulty: Math.min(4, baseDifficulty + ageProfile.difficulty_offset),
          duration: ageProfile.duration,
          material: item[2],
          objective: item[3],
          steps: buildTaskSteps(subjectCode, item[3], item[7]),
          parent_prompt: item[4],
          content: buildTaskContent(subjectCode, ageProfile, item[0], item[1], item[3], item[4], item[5]),
          tips: `${item[6]} ${SUBJECT_TIPS[subjectCode]}`,
          example_answer: item[5]
        });
      }
    }
  }
  return tasks;
}

